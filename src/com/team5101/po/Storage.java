package com.team5101.po;

public class Storage {
    private Integer id;
    private Integer storage_number;
    private Integer stock_number;
    private Integer sale_number;
    
    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public Integer getStorage_number() {
        return storage_number;
    }
    public void setStorage_number(Integer storage_number) {
        this.storage_number = storage_number;
    }
    public Integer getStock_number() {
        return stock_number;
    }
    public void setStock_number(Integer stock_number) {
        this.stock_number = stock_number;
    }
    public Integer getSale_number() {
        return sale_number;
    }
    public void setSale_number(Integer sale_number) {
        this.sale_number = sale_number;
    }
    
    
}
